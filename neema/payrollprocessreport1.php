function PrintPayroll()
{
	var EmployeeBuild = "";
	if(document.getElementById("printperpage").value == "")
	{
		alert("Select Slips per Page");
		document.getElementById("printperpage").focus();
		return false;
	}
	
	if(document.getElementById("assignmonth").value == "")
	{
		alert("Select Process Month");
		document.getElementById("assignmonth").focus();
		return false;
	}
	
	var AssignMonth = document.getElementById("assignmonth").value;
	var PrintPage = document.getElementById("printperpage").value;
	for(i=1;i<1000;i++)
	{
		if(document.getElementById("empserialnumber"+i) != null)
		{	
			if(document.getElementById("empserialnumber"+i).checked == true)
			{
				var Employeecode = document.getElementById("employeecode"+i).value;
				//alert(Employeecode);
				if(EmployeeBuild == "")
				{
					var EmployeeBuild = Employeecode;
				}
				else
				{
					var EmployeeBuild = EmployeeBuild+"||"+Employeecode+"";
				}
				//alert(EmployeeBuild);	
				
			}
		}
	}
	
	if(EmployeeBuild != '')
	{
		if(PrintPage == '2')
		{
			window.open("print_payroll1.php?employeecode="+EmployeeBuild+"&&assignmonth="+AssignMonth+"");			
		}
		else if(PrintPage == '1')
		{
			window.open("print_payrollpage1.php?employeecode="+EmployeeBuild+"&&assignmonth="+AssignMonth+"");		
		}
	}
	else
	{
		alert("Search Employee to Print");
		document.getElementById("employeesearch").focus();
		return false;
	}		
}

